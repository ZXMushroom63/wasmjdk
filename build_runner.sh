emcc ../runner/main.cpp ../wasmjdk_build/lib/libjvm.a -pthread -I../wasmjdk_build/include/ -I../wasmjdk_build/include/linux/ -o jvm.js $(cat ../export_flags) -g4 -O0 -gsource-map \
