rm -r ../runner/test/compiled
mkdir -p ../runner/test/compiled
cd ../runner/test

echo "#autogenerated, do not manually edit" > tests.txt

find . -type f -name "*.java" | while read file
do
  echo "Compiling test: $file"
  FDIR=$(dirname "$file")
  DIR=$FDIR
  FDIR=$(echo "${FDIR##*/}")
  javac $file
  mv $DIR"/Main.class" ./compiled/$FDIR.class
  echo $FDIR >> tests.txt
done

cp -r compiled/ ../../docs/compiled_tests
cp tests.txt ../../docs/tests.txt